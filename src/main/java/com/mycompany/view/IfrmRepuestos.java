    private void cargarComboMarca() {
        List<Marca> listM = mdao.obtenerMarcas();

        if (!listM.isEmpty()) {

            for (Marca marca : listM) {
                cmbMarca.addItem(marca);
            }
        } else {
            Mensajes.mensajeError("No hay Marcas almacenadas");
        }

    }

    public void creartabla() {

        String[] titulos = new String[]{"id", "repuesto", "Stock", "precio", "marca"};
        tableModel = new DefaultTableModel(titulos, 0);

        tblRepuestos.setModel(tableModel);

        List<Repuestos> ListR = rdao.obtenerRepuestos();

        if (ListR.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No hay elementos");

        } else {

            for (Repuestos r : ListR) {
                tableModel.addRow(new Object[]{r.getIdRepuestos(), r.getRepuesto(), r.getStock(),
                    r.getPrecio(), r.getMarcaIdmarca()});
            }

        }

    }


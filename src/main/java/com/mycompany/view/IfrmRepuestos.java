    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {

            if (txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "LLene el nombre y el precio");

            } else {

                repuesto.setRepuesto(txtNombre.getText());
                repuesto.setStock((int) spnStock.getValue());
                repuesto.setPrecio(new BigDecimal(txtPrecio.getText()));
                repuesto.setMarcaIdmarca(mdao.obtenerMarcaId(cmbMarca.getItemAt(cmbMarca.getSelectedIndex()).getIdmarca()));

                if (!rdao.crearRepuesto(repuesto)) {
                    JOptionPane.showMessageDialog(rootPane, "No se puede guardar el repuesto, verifique los campos");
                } else {
                    creartabla();
                }

            }

        } catch (Exception e) {
            System.out.println("vista " + e);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyTyped
        int key = evt.getKeyChar();

        boolean numeros = (key >= 48 && key <= 57) || key == 46;
        if (!numeros) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
    }//GEN-LAST:event_txtPrecioKeyTyped


